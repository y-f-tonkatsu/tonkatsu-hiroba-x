export const generateComment = () => {
    const generateNoun = () => {
        const first = FIRST_PARTS[Math.floor(Math.random() * FIRST_PARTS.length)];
        const second = SECOND_PARTS[Math.floor(Math.random() * SECOND_PARTS.length)];
        return `${first}${second}`;
    };

    const replaceName = (template: string): string => {
        return template.replace(/\{名詞\}/g, () => generateNoun());
    };

    const intro = replaceName(INTROS[Math.floor(Math.random() * INTROS.length)]);
    const main = replaceName(MAINS[Math.floor(Math.random() * MAINS.length)]);
    const outro = replaceName(OUTROS[Math.floor(Math.random() * OUTROS.length)]);

    return `${intro}\n${main}\n${outro}`;
};